    Wgpu_low.texture_create_view_configurable
      texture.handle
      label
      (Texture_format.to_int format)
      (Texture_view_dimension.to_int dimension)
      (Texture_aspect.to_int aspect)
      base_mip_level
      mip_level_count
      base_array_layer
      array_layer_count
  in
  ({ Texture_view.handle = view } : Texture_view.t)
