        (fun (entry : Bind_group_entry.t) ->
          let e = Wgpu_low.Bind_group_entry.bind_group_entry_create () in
          Wgpu_low.Bind_group_entry.bind_group_entry_set_binding e entry.binding;
          Wgpu_low.Bind_group_entry.bind_group_entry_set_buffer
            e
            (match entry.buffer with
             | Some x -> x.Buffer.handle
             | None -> 0n);
          Wgpu_low.Bind_group_entry.bind_group_entry_set_offset e entry.offset;
          Wgpu_low.Bind_group_entry.bind_group_entry_set_size e entry.size;
          Wgpu_low.Bind_group_entry.bind_group_entry_set_sampler
            e
            (match entry.sampler with
             | Some x -> x.Sampler.handle
             | None -> 0n);
          Wgpu_low.Bind_group_entry.bind_group_entry_set_texture_view
            e
            (match entry.texture_view with
             | Some x -> x.Texture_view.handle
             | None -> 0n);
          e)
        entries
    in
    let entries_array = Array.of_list entry_structs in
    Wgpu_low.Bind_group_descriptor.bind_group_descriptor_set_entries desc entries_array;
    let bind_group = Wgpu_low.device_create_bind_group t.handle desc in
    (* Free entry structs *)
    List.iter (fun e -> Wgpu_low.Bind_group_entry.bind_group_entry_free e) entry_structs;
    Wgpu_low.Bind_group_descriptor.bind_group_descriptor_free desc;
    ({ Bind_group.handle = bind_group } : Bind_group.t)
  ;;

  let create_pipeline_layout t ?(label = "") ~bind_group_layouts () =
    let desc = Wgpu_low.Pipeline_layout_descriptor.pipeline_layout_descriptor_create () in
    Wgpu_low.Pipeline_layout_descriptor.pipeline_layout_descriptor_set_label desc label;
    let layouts_array =
      Array.of_list (List.map (fun x -> x.Bind_group_layout.handle) bind_group_layouts)
    in
    Wgpu_low.Pipeline_layout_descriptor.pipeline_layout_descriptor_set_bind_group_layouts
      desc
      layouts_array;
    let layout = Wgpu_low.device_create_pipeline_layout t.handle desc in
    Wgpu_low.Pipeline_layout_descriptor.pipeline_layout_descriptor_free desc;
    ({ Pipeline_layout.handle = layout } : Pipeline_layout.t)
  ;;

  let poll t ?(wait = false) () = Wgpu_low.device_poll t.handle wait
end

module Adapter = struct
  type t = { handle : Wgpu_low.adapter }

  let get_info t = Adapter_info.of_low (Wgpu_low.adapter_get_info t.handle)
  let release t = Wgpu_low.adapter_release t.handle

  let request_device t =
    let device = Wgpu_low.adapter_request_device_sync t.handle in
    { Device.handle = device }
  ;;

  let has_feature t ~feature =
    Wgpu_low.adapter_has_feature t.handle (Feature_name.to_int feature)
  ;;
end

module Instance = struct
  type t = { handle : Wgpu_low.instance }

  let create () = { handle = Wgpu_low.create_instance () }
  let release t = Wgpu_low.instance_release t.handle

  let request_adapter
    t
    ?(power_preference = Power_preference.Undefined)
    ?(backend_type = Backend_type.Undefined)
    ()
    =
    let adapter =
      Wgpu_low.instance_request_adapter_sync
        t.handle
        (Power_preference.to_int power_preference)
        (Backend_type.to_int backend_type)
    in
    { Adapter.handle = adapter }
  ;;
end

(* Convenience functions for methods that take complex descriptors *)

let begin_compute_pass (encoder : Command_encoder.t) ?(label = "") () =
  let desc = Wgpu_low.Compute_pass_descriptor.compute_pass_descriptor_create () in
  Wgpu_low.Compute_pass_descriptor.compute_pass_descriptor_set_label desc label;
  let pass = Wgpu_low.command_encoder_begin_compute_pass encoder.handle desc in
  Wgpu_low.Compute_pass_descriptor.compute_pass_descriptor_free desc;
  ({ Compute_pass_encoder.handle = pass } : Compute_pass_encoder.t)
;;

let begin_render_pass
  (encoder : Command_encoder.t)
  ?(label = "")
  ~color_view
  ?(load_op = Load_op.Clear)
  ?(store_op = Store_op.Store)
  ~clear_color
  ()
  =
  let r, g, b, a = clear_color in
  let pass =
    Wgpu_low.command_encoder_begin_render_pass_configurable
      encoder.handle
      label
      color_view.Texture_view.handle
      (Load_op.to_int load_op)
      (Store_op.to_int store_op)
      r
      g
      b
      a
  in
  ({ Render_pass_encoder.handle = pass } : Render_pass_encoder.t)
;;

let finish (encoder : Command_encoder.t) ?(label = "") () =
  Command_encoder.finish encoder ~label ()
;;

